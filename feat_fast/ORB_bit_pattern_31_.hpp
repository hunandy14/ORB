	inline void angle_trans(float& x, float& y, float sita){
		const double M_PI = 3.14159265358979323846;
		// 預算
		sita = -sita;
		float cos_t = cosf(sita*(float)(M_PI/180));  
		float sin_t = sinf(sita*(float)(M_PI/180));
		// 輸入新圖座標返回舊圖座標(已0, 0為圓心旋轉)
		float r_rot = - (y)*sin_t + (x)*cos_t; // 原圖X座標
		float c_rot =   (y)*cos_t + (x)*sin_t; // 原圖Y座標
		x = round(r_rot);
		y = round(c_rot);
	}
